#include "./libft.h"

int ft_atoi(const char *str)
{
	int i;
	int sgn;
	int res;

	sgn = 1;
	i = 1;
	res = 0;

	if (str[0] == '-')
	{
		sgn *= -1;
	}
	while (str[i] != 0)
	{
		res = res * 10 + (str[i] + '0');
		i++;		
	}	
	return (res * sgn);
}